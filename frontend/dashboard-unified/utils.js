const { apiService, authService } = window;

export function getStoredToken() {
  return authService.getToken();
}

export function hasAuthToken() {
  return authService.isLoggedIn();
}

export function updateCurrentUser(userData) {
  if (!userData) return;
  authService.updateUser(userData);
}

export function getCurrentUser() {
  return authService.getUser();
}

export function getUserRole() {
  return authService.getRole() || "donor";
}

export function ensureDashboardSession() {
  if (!hasAuthToken()) {
    const redirectTarget = encodeURIComponent("/pages/dashboard-unified.html");
    window.location.href = `/pages/login.html?redirect=${redirectTarget}`;
    return false;
  }
  return true;
}

export function escapeHtml(value) {
  if (value === null || value === undefined) return "";
  return String(value)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");
}

export function formatNumber(value) {
  const numeric = Number(value || 0);
  return Number.isFinite(numeric) ? numeric.toLocaleString() : "0";
}

export function formatDate(value) {
  if (!value) return "-";
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) return "-";
  return date.toLocaleDateString("en-US", { year: "numeric", month: "short", day: "numeric" });
}

export function formatDateTime(value) {
  if (!value) return "-";
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) return "-";
  return date.toLocaleString("en-US", { year: "numeric", month: "short", day: "numeric", hour: "2-digit", minute: "2-digit" });
}

export function toTitleCase(value) {
  if (!value) return "-";
  return String(value).replace(/[_-]+/g, " ").trim().replace(/\b\w/g, (char) => char.toUpperCase());
}

export function getStatusClass(status) {
  const normalized = String(status || "").toLowerCase();
  if (["closed", "completed", "delivered", "fulfilled"].includes(normalized)) return "status-success";
  if (["pending", "broadcasted", "assigned"].includes(normalized)) return "status-warning";
  if (["claimed", "approved", "active"].includes(normalized)) return "status-info";
  if (["cancelled", "failed", "rejected", "expired"].includes(normalized)) return "status-danger";
  return "status-neutral";
}

export function getStatusBadge(status) {
  return `<span class="status-badge ${getStatusClass(status)}">${escapeHtml(toTitleCase(status || "Unknown"))}</span>`;
}

export function initialsFromName(user) {
  if (!user) return "U";
  const source = [user.firstName, user.lastName, user.name].find(Boolean) || "User";
  const parts = String(source).trim().split(/\s+/).filter(Boolean);
  if (!parts.length) return "U";
  if (parts.length === 1) return parts[0].slice(0, 2).toUpperCase();
  return `${parts[0][0] || ""}${parts[1][0] || ""}`.toUpperCase();
}

export const apiGet = (endpoint) => apiService.get(endpoint);
export const apiPost = (endpoint, body) => apiService.post(endpoint, body);
export const apiPut = (endpoint, body) => apiService.put(endpoint, body);
export const apiDelete = (endpoint) => apiService.delete(endpoint);

export function renderInlineState(message, tone = "info") {
  return `<div class="dashboard-inline-state dashboard-inline-${tone}">${escapeHtml(message)}</div>`;
}

